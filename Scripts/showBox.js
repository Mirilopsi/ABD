'use strict';

function mostrarFormNuevoMensaje() {
    let panelNuevoMensaje = document.getElementById('nuevo-mensaje');
    panelNuevoMensaje.style.visibility = "visible";
}


function esconderFormNuevoMensaje() {
    let panelNuevoMensaje = document.getElementById('nuevo-mensaje');
    panelNuevoMensaje.style.visibility = "hidden";
}